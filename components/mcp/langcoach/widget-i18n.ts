export function useT(locale: Locale) {
  const s = STRINGS[locale] ?? STRINGS.en
  return (key: string, vars?: Record<string, string | number>) => {
    let v = s[key] ?? key
    if (vars) {
      for (const [k, val] of Object.entries(vars)) {
        v = v.replace(`{${k}}`, String(val))
      }
    }
    return v
  }
}
